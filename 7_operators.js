// Assignment
// = is assignment operator

let a = 60;
let b = 15;

// arrithmatic

// add
let result = a + b; // 75

// string
// let x = "Puja";
// let y = "Gupta";

// const fullName = x + " " + y; // Puja Gupta

// substract
result = a - b; // 45

// multiplication
result = a * b; // 900

// division
result = a / b; // 4

// remainder
result = a % b;

// comparison
// equal (==)
result = a == b; //false

// not-equal (!=)
result = a != b; //true

// strict equal (===)
result = a === "60"; //false
result = a === 60; //true

// not-equal (!==)
result = a !== "60"; //true
result = a !== 60; //true

// greater than (>)
result = a > b; //true

// greater than (>=)
result = a >= b; //true

// less than (>)
result = a < b; //false

// less than (>=)
result = a <= b; //false

// Other important
// increment
result = ++a; // pre-increment
result = a++; // post-increament

// decrement
result = --a; // pre-decrement
result = a++; // post-decreament

// not (!)
result = !true;

// unary negation (-)
result = -"3";

// unary plus
result = +"3";
result = +true;

console.log("Result: ", result);
